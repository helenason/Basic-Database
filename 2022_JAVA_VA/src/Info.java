// Ctrl + Shift + F : 정렬
// Alt + Shift + A : 다중편집기능

// Ctrl + Alt + 방향키 : 복사
// Ctrl + D : 줄 삭제
// Shift+ Enter : 줄 추가
// Ctrl + Shift + C : 개별 주석
// Ctrl + Shift + / : 묶음 주석 <-> 주석 해제 (\)

// Ctrl + 클릭 : 위치 찾기

public class Info {
	public static void main(String[] args) { // main 치고 Ctrl + Shift
		char b = '안'; // '를 사용해야함
		String c = "안녕하세요"; // "를 사용해야함 String

		String a = "1\n";
		a += "2\n";
		a += "3\n";
		System.out.println(a + b + c);
		
	}
}
